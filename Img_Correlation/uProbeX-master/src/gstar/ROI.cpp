#include <gstar/ROI.h>

using namespace gstar;

/*---------------------------------------------------------------------------*/

ROI::ROI()
: m_x(-1.0),
  m_y(-1.0),
  m_w(-1.0),
  m_h(-1.0),
  m_color("blue"),
  m_text("")
{

}

/*---------------------------------------------------------------------------*/

ROI::ROI(double x, double y, double w, double h, QString color, QString text)
: m_x(x),
  m_y(y),
  m_w(w),
  m_h(h),
  m_color(color),
  m_text(text)
{

}

/*---------------------------------------------------------------------------*/

QString ROI::getColor()
{

   return m_color;

}

/*---------------------------------------------------------------------------*/

double ROI::getHeight()
{

   return m_h;

}

/*---------------------------------------------------------------------------*/

QString ROI::getText()
{

   return m_text;

}

/*---------------------------------------------------------------------------*/

double ROI::getWidth()
{

   return m_w;

}

/*---------------------------------------------------------------------------*/

double ROI::getX()
{

   return m_x;

}

/*---------------------------------------------------------------------------*/

double ROI::getY()
{

   return m_y;

}

/*---------------------------------------------------------------------------*/

void ROI::setColor(QString color)
{

   m_color = color;

}

/*---------------------------------------------------------------------------*/

void ROI::setHeight(double h)
{

   m_h = h;

}

/*---------------------------------------------------------------------------*/

void ROI::setText(QString text)
{

   m_text = text;

}

/*---------------------------------------------------------------------------*/

void ROI::setWidth(double w)
{

   m_w = w;

}

/*---------------------------------------------------------------------------*/

void ROI::setX(double x)
{

   m_x = x;

}

/*---------------------------------------------------------------------------*/

void ROI::setY(double y)
{

   m_y = y;

}

/*---------------------------------------------------------------------------*/
